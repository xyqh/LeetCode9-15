bool canConstruct(string ransomNote, string magazine){
    vector<int> hash(26, 0);
    for(char c:magazine){
        ++hash[c - 'a'];
    }
    cout << hash[0] << endl;

    for(char c:ransomNote){
        if(--hash[c - 'a'] < 0){
            return false;
        }
    }

    return true;
}

